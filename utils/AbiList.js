  // This function swaps an exact amount of input tokens for as many output tokens as possible
  // It takes the following parameters:
  // - amountIn: the amount of input tokens to send
  // - amountOutMin: the minimum amount of output tokens that must be received for the transaction not to revert
  // - path: an array of token addresses (the swap route)
  // - to: the address to send the output tokens to
  // - deadline: a timestamp by which the transaction must be completed, otherwise it will revert
  // It returns an array of amounts, representing the amount of each token received at each step of the swap
  // It is marked as 'external', meaning it can be called from outside the contract
